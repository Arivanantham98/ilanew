                                    <h3>Power attorney</h3>
                                    <div class="row mt-15 mb-30 px-4">
                                        <p>It is a legal documents executed by one or more persons giving an authority to
                                            one or more persons to act on his behalf. The person giving authority is called
                                            attorney. </p>
                                        <h5>
                                            What Does power of attorney allow you to do?</h5>
                                        <p>A Power of Attorney might be used to allow another person to sign a contract for
                                            the Principal. It can be used to give another person the authority to make
                                            health care decisions, do financial transactions, or sign legal documents that
                                            the Principal cannot do for one reason or another.
                                        </p>

                                        <h5>
                                            General Power of Attorney</h5>
                                        <p>In this situation, the agent can perform almost any act as the principal, such as opening financial accounts and managing personal finances. A general power of attorney arrangement is terminated when the principal becomes incapacitated, revokes the power of attorney or passes away. 
                                        </p>


                                        <h5>
                                            Durable Power of Attorney.</h5>
                                        <p>This arrangement designates another person to act on the principal’s behalf and includes a durable clause that maintains the power of attorney after the principal becomes incapacitated.
                                        </p>

                                        <h5>
                                            Special or Limited Power of Attorney</h5>
                                        <p>In this instance, the agent has specific powers limited to a certain area. An example is a power of attorney that grants the agent authority to sell a home or other piece of real estate. </p>

                                        <h5>Springing Durable Power of Attorney</h5>
                                        <p>In some states, a “springing” power of attorney is available and becomes effective when a specified event occurs such as when the principal becomes incapacitated. 
                                        </p>
                                        <p>It’s helpful to have these conversations in happy times, when your loved one is well so you can determine their wishes for their financial security and healthcare should a time come when they are unable to make the choices for themselves.
                                            You and your loved one can get help with power of attorney questions by consulting an elder law attorney. The National Academy of Elder Law Attorneys can help you find a local professional for your area.
                                      
